number = int(input("Give me a number: "))

reminder = number % 2

if reminder == 0:
  print("The number is even :)")
else:
  print("The number is odd!")

#check if a number is a multiple of 4
#print message for multiple of 4
multiple = number % 4

if multiple == 0:
  print("Yes! The number is a multiple of four")
else:
  print("Oops! The number is not a multiple of four")





